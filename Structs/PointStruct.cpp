#include "pch.h"
#include "PointStruct.h"
#include "Point3D.h"


///////////////////////////////////////////////////////////////////////
PointStruct::PointStruct()
{
	xyz[0] = 0;
	xyz[1] = 0;
	xyz[2] = 0;

}
///////////////////////////////////////////////////////////////////////
PointStruct::PointStruct(PointStruct* that)
{
	if (!that)
		return;

	this->xyz[0] = that->xyz[0];
	this->xyz[1] = that->xyz[1];
	this->xyz[2] = that->xyz[2];
}
///////////////////////////////////////////////////////////////////////
PointStruct::PointStruct(PointStruct& that)
{
	this->xyz[0] = that.xyz[0];
	this->xyz[1] = that.xyz[1];
	this->xyz[2] = that.xyz[2];
}
///////////////////////////////////////////////////////////////////////
PointStruct::PointStruct(double* xyz, long id)
{
	SetXYZValue(xyz);
	SetID(id);
}
///////////////////////////////////////////////////////////////////////
PointStruct::PointStruct(Point3D&pnt)
{
	this->xyz[0] = pnt.x;
	this->xyz[1] = pnt.y;
	this->xyz[2] = pnt.z;
}
///////////////////////////////////////////////////////////////////////
PointStruct::~PointStruct()
{

}
///////////////////////////////////////////////////////////////////////
void PointStruct::operator=(PointStruct& that)
{
	this->xyz[0] = that.xyz[0];
	this->xyz[1] = that.xyz[1];
	this->xyz[2] = that.xyz[2];
}
///////////////////////////////////////////////////////////////////////
void PointStruct::SetXYZValue(double x, double y, double z)
{
	this->xyz[0] = x;
	this->xyz[1] = y;
	this->xyz[2] = z;
}
///////////////////////////////////////////////////////////////////////
void PointStruct::SetXYZValue(double* xyz)
{
	if (!xyz)
		return;

	this->xyz[0] = xyz[0];
	this->xyz[1] = xyz[1];
	this->xyz[2] = xyz[2];
}
///////////////////////////////////////////////////////////////////////
void PointStruct::SetID(long id)
{
	this->id = id;
}
///////////////////////////////////////////////////////////////////////
double* PointStruct::GetXYZValue()
{
	return xyz;
}
///////////////////////////////////////////////////////////////////////
long PointStruct::GetID()
{
	return id;
}
